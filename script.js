function hurufKeAngka(huruf){
	switch(huruf){
		case "A": return 4;
		break;
		case "AB": return 3.5;
		break;
		case "B": return 3;
		break;
		case "BC": return 2.5;
		break;
		case "C": return 2;
		break;
		case "D": return 1;
		break;
		case "E": return 0;
		break;
	}
}

